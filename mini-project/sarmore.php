        <p><br><b>Date of Birth : </b>31 October 1875<br>
		<br><b>Place of Birth :</b> Nadiad, Bombay Presidency (present day Gujarat)<br>
<br><b>Parents :</b> Zaverbhai Patel (father) and Ladbai (mother)<br>
<br><b>Spouse :</b> Jhaverba<br>
<br><b>Children :</b> Maniben Patel, Dahyabhai Patel<br>
<br><b>Education :</b> N. K. High school, Petlad; Inns of Court, London, England<br>
<br><b>Association :</b> Indian National Congress<br>
<br><b>Movement :</b> Indian Independence Struggle<br>
<br><b>Political Ideology :</b> Moderate, Right-wing<br>
<br><b>Religious Beliefs :</b> Hinduism<br>
<br><b>Publications :</b> Ideas of a Nation: Vallabhai Patel, The Collected Works of Vallabhbhai Patel, 15 volumes<br>
<br><b>Passed Away :</b> 15 December 1950<br>
<br>Vallabhbhai Patel was an Indian barrister, statesman and a prominent 
leader of the Indian Independence movement. Popularly known as 
Sardar Patel and The Ironman of India, he was the first Deputy Prime 
Minister and the first Home Minister of independent India.<br>
<br>After studying law in England, he practiced law in Ahmedabad. Initially 
not much interested in the independence movement, a meeting with 
Mahatma Gandhi in 1917 changed his views. Leaving his law-practice, 
Patel committed himself to the Independence struggle. He received the 
title of Sardar (leader/Chief) after successfully leading the agitation of the 
farmers of Bardoli (1928). His greatest contribution to the post independence India was the integration of 565 princely states, and 
creation of All-India Services. In 1991, India’s highest civilian 
award, Bharat Ratna was conferred upon him posthumously.<br>
<br><b>Childhood & Early Life</b><br>
Sardar Patel was born Vallabhbhai Jhaverbhai Patel in 1875, at Nadiad, 
Gujarat, British India, in a middle-class agricultural family of Lewa Patidar 
community. There is no official record of his date of birth, but October 31 is mentioned as his date of birth on his matriculation exam papers. He 
was the fourth of six children of Jhaverbhai Patel and his wife, Laadbai. 
His father had participated in the 1857 Mutiny, in the army of Rani Laxmi 
of Jhansi.<br>
<br>Growing up in a traditional Hindu family, his early childhood was spent 
on family’s agricultural fields at Karamsad. By late teens, he completed 
his middle school education at Karamsad. In 1891, he was married to 
Jhaverba when he was 16. At 22, he completed his matriculation from a 
high school in Nadiad/Petlad in 1897.<br>
<br>Patel aimed to work and collect necessary money to go to England to 
study law. After schooling, he studied by borrowing law books and 
passed the District Pleader’s examination. In 1900, he started his law 
practice at Godhra. He brought his wife, Jhaverba, from her parent’s 
place, and together, they set up a home. They had two children: a 
daughter, Maniben (B.1904), and a son, Dahyabhai (b.1906).<br>
<br>
With his hard work and dedication, Patel became a capable lawyer. 
During a plague epidemic, he contracted the disease while nursing a 
friend. Leaving his family, he went to Nadiad to recuperate.<br>
<br>
In 1902, Patel moved to Borsad (Kheda district) to practice law, where 
he successfully handled challenging court cases. With his law practice, 
he saved enough money to go to England to study law. The ticket bore 
the name ‘V.J. Patel,’ which were also the initials of his elder brother, 
Vithalbhai Patel. After learning about his elder brother’s wish to study in 
England, Vallabhbhai decided that his elder brother should go first, so as 
to maintain family’s reputation.<br>
<br><b>Role in the Indian National Movement</b><br>
Joining India’s independence movement, Patel motivated the people of 
Borsad in September 1917 to join Gandhi’s demand for independence. 
Patel joined the Indian National Congress’ Gujarat Sabha as secretary 
and helped in Gandhi’s campaigns.<br>
<br>
Kheda district faced a plague epidemic in 1917, followed by a famine in 
1918. Despite crop failures, the British government refused to exempt 
the land revenue. Patel led the agitation of the farmers and Zamindars 
to get tax exemption. During the 3-month long campaign, he came very close to Gandhi. Visiting several villages, he motivated farmers to revolt
against the government without any violence by not paying taxes. 
Several farmers and volunteers were arrested, lands were seized and 
people faced harassment, but the resistance effort paid off and the 
government was forced to exempt the taxes.<br>
<br>In 1920, Patel was elected President of the Gujarat Pradesh Congress 
Committee (the post he served till 1945). Leaving his successful legal 
practice, he joined Gandhi’s Non-Cooperation Movement in 1920. He 
and his children burned their western clothing in bon-fires organized to 
burn and boycott British goods. He started wearing Indian attire made of 
Khadi (Indian handloom cotton). He traveled from place to place and 
recruited 3,00,000 members and collected a fund of Rs.1.5 million.<br>
<br>
In 1923, while Gandhi was in prison, Patel led the Satyagraha 
Movement in Nagpur, when the British banned hoisting of Indian flag. He 
succeeded in obtaining the consent to hoist the flag publicly, and also 
got the prisoners released (arrested for hoisting the flag).<br>
<br>
From 1924-1928, Patel was chosen the President of the municipal 
committee of Ahmedabad. During these years, he implemented several 
sanitation, water supply, administration and town planning programs. He 
also worked towards several social reforms, including prohibition of 
untouchability, casteism, alcoholism, etc.<br>
<br>
In 1928, the government raised the land revenue at a time when the 
farmers in Bardoli taluka of Surat district were already facing famine. 
Patel toured the villages to gauge the condition. Before launching 
a Satyagraha, he forewarned the villagers of the difficulties and asked 
them to maintain non-violence and unity.<br>
<br>
As per Patel’s call for a non-cooperation movement on February 12, 
1928, the farmers refused to pay the taxes demanded by the 
government. The government responded by arresting farmers and 
confiscating their lands, but the farmers didn’t give-in. 
Many Satyagrahas were undertaken all over Gujarat to express 
solidarity and sympathy with the Bardoli farmers. The agitation 
continued for 6 months, while Patel carried on his negotiations with the 
government. His efforts bore fruit in August and the administration 
returned the seized lands and the implementation of increased tax was postponed. The success of the Bardoli Satyagraha earned him the 
name Sardar or chief.<br>
<br>In 1930, Gandhi gave the call for Dandi March and Salt Satyagraha to 
protest against the salt tax. As one of the leaders, Patel was arrested 
before the Dandi March on March 7, 1930. He was tried without any 
witnesses or lawyers. After Gandhi’s arrest, the agitation intensified 
demanding release of the two leaders. Patel was released in June and 
took on the responsibilities as the Congress president in absence of 
Gandhi. However, he was arrested once again.<br>
<br>
Patel was elected the President of the 46th session of the Indian National 
Congress at Karachi, in March 1931. Congress approved the Gandhi Irwin Pact, though Nehru and Bose didn’t totally agree with the terms of 
the pact. The same day, Bhagat Singh and associates were executed in 
Lahore. The Karachi session of the Congress coped with a lot of turmoil. 
Thereafter, the Indian National Congress agreed to participate in 
the Round Table Conference in London. However, the Conference didn’t 
succeed and subsequently Gandhi, Patel and several other leaders were 
arrested. Patel was with Gandhi in Yerwada Jail from January 1931 to 
May 1933. When Gandhi protested against allocation of separate 
electorates for untouchables by starting fast-unto-death, Patel looked 
after him. Later, he was shifted to Nasik Jail for one year, and was 
released in 1934.<br>
<br>
As per the Government of India Act 1935, the Congress decided to 
participate in the elctions to provincial legislatures. Patel played an 
important role in raising funds and selecting candidates for these 
elections. The Indian National Congress won in 7 out of 11 provinces. As 
the chairman of the Congress Parliamentary Sub-committee, he guided 
the ministries.<br>
<br>
At the start of WWII, the Viceroy declared India as an ally of England. 
The Congress ministries resigned in protest and leaders courted arrests. 
Gandhi gave call for Individual Civil Disobedience. After being arrested 
in November 1940, Patel was later released on August 29, 1941, due to 
ill-health.<br>
<br>
On August 8, 1942, the All India Congress Committee launched the Quit 
India Movement. Many prominent Congress leaders, including Patel, 
were arrested on August 9, 1942. Patel was arrested and confined at the Ahmednagar Fort for 3 years. All Congress leaders were released in 
1945, after the end of WWII.<br>
	
