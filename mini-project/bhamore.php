<br><b>Personal Life of Bhagat Singh-</b><br>
One of the most prominent revolutionaries of India, Bhagat 
Singh was born on 28 September 1907 in a Sikh family in the 
village of Banga in Layalpur district of present-day Pakistan. 
The third son of Sardar Kishan Singh and Vidyavati, Bhagat 
Singh’s father and uncle were members of Ghadar party.<br>
<br><b>Influences on Bhagat Singh-</b>
<br>He was greatly attracted towards socialism. Believed to be one 
of India’s earliest Marxists, Bhagat Singh was one of the leaders 
and founders of the Hindustan Socialist Republican Association 
(HSRA). Bhagat Singh was deeply saddened by the Jallianwala 
Bagh massacre of 1919. Though he participated in the non cooperation movement, he was disappointed when Gandhi called 
off the agitation after the Chauri Chaura incident. He studied at 
the National College in Lahore where he came into contact with 
other revolutionaries such as Bhagwati Charan, Sukhdev and 
others. He fled from home to escape early marriage and became 
a member of the organisation Naujawan Bharat Sabha.<br>
<br>
<b>Deeds of Bhagat Singh-</b>
<br>Bhagat Singh was against individual acts of terrorism and gave 
a clarion call for mass mobilisation. In 1928, he came in contact 
with another famous revolutionary Chandrasekhar Azad. The 
two combined to form the ‘Hindustan Samajvadi Prajatantra 
Sangha’. During the Simon Commission’s visit to India in 
February 1928, there were protests against the Simon 
Commission’s visit to Lahore. In one of these protests, Lala 
Lajpat Rai was injured in a lathi charge and later on succumbed 
to his injuries. To avenge Lajpat Rai’s death, Bhagat Singh 
decided to kill the British official responsible for the killing, 
Deputy Inspector General Scott. But he accidentally shot 
Assistant Superintendent Saunders instead, mistaking him for 
Scott.<br>
Bhagat Singh threw a bomb in the Central Legislative Assembly 
on 8 April 1929 and thereafter courted arrest. Bhagat Singh, 
Sukh Dev and Raj Guru were awarded death sentence by a court 
for their subversive activities. They were hanged on 23 March 
1931. Bhagat Singh is still seen as the role model by a large number of young people in India. His sense of sacrifice, 
patriotism and courage are something that will be revered and 
looked upon by generations to come.<br>
	
