        <p><br>Jawaharlal Nehru, byname Pandit (Hindi: “Pundit” or “Teacher”) 
Nehru, (born November 14, 1889, Allahabad, India—died May 27, 
1964, New Delhi), first prime minister of independent India (1947–
64), who established parliamentary government and became noted 
for his neutralist (nonaligned) policies in foreign affairs. He was 
also one of the principal leaders of India’s independence movement 
in the 1930s and ’40s.<br>
<br>Nehru was born to a family of Kashmiri Brahmans, noted for their 
administrative aptitude and scholarship, who had migrated 
to Delhi early in the 18th century. He was a son of Motilal Nehru, a 
renowned lawyer and leader of the Indian independence movement, 
who became one of Mohandas (Mahatma) Gandhi’s prominent 
associates. Jawaharlal was the eldest of four children, two of whom 
were girls. A sister, Vijaya Lakshmi Pandit, later became the first 
woman president of the United Nations General Assembly.<br>
<br>Until the age of 16, Nehru was educated at home by a series of 
English governesses and tutors. Only one of those—a part-Irish, 
part-Belgian theosophist, Ferdinand Brooks—appears to have made 
any impression on him. Jawaharlal also had a venerable Indian 
tutor who taught him Hindi and Sanskrit. In 1905 he went 
to Harrow, a leading English school, where he stayed for two years. 
Nehru’s academic career was in no way outstanding. From Harrow 
he went to Trinity College, Cambridge, where he spent three years 
earning an honours degree in natural science. On leaving 
Cambridge he qualified as a barrister after two years at the Inner 
Temple, London, where in his own words he passed his 
examinations “with neither glory nor ignominy.”<br>
<br><b>Born :</b> November 14, 1889 Prayagraj India<br>
<br><b>Died : </b>May 27, 1964 Delhi India<br>
<br><b>Title / Office:</b> prime minister (1947-1964), India<br>
<br><b>Political Affiliation:</b> Indian National Congress<br>
<br>The seven years Nehru spent in England left him in a hazy half world, at home neither in England nor in India. Some years later he 
wrote, “I have become a queer mixture of East and West, out of 
place everywhere, at home nowhere.” He went back to India to 
discover India. The contending pulls and pressures that his 
experience abroad were to exert on his personality were never 
completely resolved.<br>
<br>Four years after his return to India, in March 1916, Nehru married 
Kamala Kaul, who also came from a Kashmiri family that had 
settled in Delhi. Their only child, Indira Priyadarshini, was born in 
1917; she would later (under her married name of Indira Gandhi) 
also serve (1966–77 and 1980–84) as prime minister of India. In 
addition, Indira’s son Rajiv Gandhi succeeded his mother as prime 
minister (1984–89).<br>
<br><b>Struggle for Indian independence</b><br>
<br>After the Lahore session of 1929, Nehru emerged as the leader of 
the country’s intellectuals and youth. Gandhi had shrewdly elevated 
him to the presidency of the Congress Party over the heads of some 
of his seniors, hoping that Nehru would draw India’s youth—who at 
that time were gravitating toward extreme leftist causes—into the 
mainstream of the Congress movement. Gandhi also correctly 
calculated that, with added responsibility, Nehru himself would be 
inclined to keep to the middle way.<br>
<br>After his father’s death in 1931, Nehru moved into the inner councils 
of the Congress Party and became closer to Gandhi. Although 
Gandhi did not officially designate Nehru his political heir until 
1942, the Indian populace as early as the mid-1930s saw in Nehru 
the natural successor to Gandhi. The Gandhi-Irwin Pact of March 
1931, signed between Gandhi and the British viceroy, Lord Irwin 
(later Lord Halifax), signalized a truce between the two principal 
protagonists in India. It climaxed one of Gandhi’s more effective civil disobedience movements, launched the year before as 
the Salt March, in the course of which Nehru had been arrested.<br>
<br>Hopes that the Gandhi-Irwin Pact would be the prelude to a more relaxed period of Indo-British relations were not borne out; Lord 
Willingdon (who replaced Irwin as viceroy in 1931) jailed Gandhi in 
January 1932, shortly after Gandhi’s return from the second Round 
Table Conference in London. He was charged with attempting to 
mount another civil disobedience movement; Nehru was also 
arrested and sentenced to two years’ imprisonment.<br>

<br>The three Round Table Conferences in London, held to advance
India’s progress to self-government, eventually resulted in 
the Government of India Act of 1935, which gave the Indian 
provinces a system of popular autonomous government. Ultimately, 
it provided for a federal system composed of the autonomous 
provinces and princely states. Although federation never came into 
being, provincial autonomy was implemented. During the mid 1930s Nehru was much concerned with developments in Europe, 
which seemed to be drifting toward another world war. He was in 
Europe early in 1936, visiting his ailing wife, shortly before she died 
in a sanitarium in Lausanne, Switzerland. Even at that time he 
emphasized that in the event of war India’s place was alongside 
the democracies, though he insisted that India could fight in 
support of Great Britain and France only as a free country.<br>

<br>When the elections following the introduction of provincial 
autonomy brought the Congress Party to power in a majority of the 
provinces, Nehru was faced with a dilemma. The Muslim 
League under Mohammed Ali Jinnah (who was to become the 
creator of Pakistan) had fared badly at the polls. Congress, 
therefore, unwisely rejected Jinnah’s plea for the formation of 
coalition Congress–Muslim League governments in some of the 
provinces, a decision that Nehru had supported. The subsequent 
clash between the Congress and the Muslim League hardened into a 
conflict between Hindus and Muslims that was ultimately to lead to 
the partition of India and the creation of Pakistan.<br>
	
