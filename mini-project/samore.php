<br>Sarojini was the eldest daughter of Aghorenath Chattopadhyay, a Bengali Brahman who was principal of the Nizam’s College, Hyderabad. She entered the University of Madras at the age of 12 
and studied (1895–98) at King’s College, London, and later at 
Girton College, Cambridge.<br>
<br>
After some experience in the suffragist campaign in England, she was drawn to India’s Congress movement and to Mahatma Gandhi’s Noncooperation Movement. In 1924 she traveled in eastern Africa and South Africa in the interest of Indians there and the following year became the first Indian woman president of 
the National Congress—having been preceded eight years earlier by 
the English feminist Annie Besant. She toured North America, 
lecturing on the Congress movement, in 1928–29. Back in India her anti-British activity brought her a number of prison sentences 
(1930, 1932, and 1942–43). She accompanied Gandhi to London for 
the inconclusive second session of the Round Table Conference for 
Indian–British cooperation (1931). Upon the outbreak of World 
War II she supported the Congress Party’s policies, first of 
aloofness, then of avowed hindrance to the Allied cause. In 1947 she 
became governor of the United Provinces (now Uttar Pradesh), a 
post she retained until her death.<br>
<br>
Sarojini Naidu also led an active literary life and attracted notable 
Indian intellectuals to her famous salon in Bombay (now Mumbai). 
Her first volume of poetry, The Golden Threshold (1905), was 
followed by The Bird of Time (1912), and in 1914 she was elected a 
fellow of the Royal Society of Literature. Her collected poems, all of 
which she wrote in English, have been published under the 
titles The Sceptred Flute (1928) and The Feather of the 
Dawn (1961).<br>
	
